package com.company.model;

public class CuTri {
    private int i;
    private int a1;
    private int a2;
    private int a3;
    private int a4;

    public CuTri(int i, int a1, int a2, int a3, int a4) {
        this.i = i;
        this.a1 = a1;
        this.a2 = a2;
        this.a3 = a3;
        this.a4 = a4;
    }

    public CuTri() {}

    public int getI() {
        return i;
    }

    public void setI(int i) {
        this.i = i;
    }

    public int getA1() {
        return a1;
    }

    public void setA1(int a1) {
        this.a1 = a1;
    }

    public int getA2() {
        return a2;
    }

    public void setA2(int a2) {
        this.a2 = a2;
    }

    public int getA3() {
        return a3;
    }

    public void setA3(int a3) {
        this.a3 = a3;
    }

    public int getA4() {
        return a4;
    }

    public void setA4(int a4) {
        this.a4 = a4;
    }
}
