package com.company.model;

public class MaHoa {
    private double x;
    private double y;
    private double h;

    public MaHoa(double x, double y,double h) {
        this.x = x;
        this.y = y;
        this.h = h;
    }

    public double getX() {
        return x;
    }

    public double getY() {
        return y;
    }

    public double getH(){
        return h;
    }

}
